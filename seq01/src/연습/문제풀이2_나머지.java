package 연습;

public class 문제풀이2_나머지 {

	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 40;
		
		System.out.println( num1 + num2 );
		System.out.println( num1 / num2 );
		// 30/40은 정수에서 불가능하므로 0의 결과값이 출력됨
		System.out.println( num1 % num2 );
		// %는 나머지를 구하는 명령어
	}

}
